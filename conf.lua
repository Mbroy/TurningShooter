-- Configuration
function love.conf(t)
    t.title = "Scroll Shooter Tutorial" --The title of the window the game is in (string)
    t.version = "0.10.1" --The LOVE version this game was made for (string)
    t.window.width = 800 
    t.window.height = 800

    -- For Windows debugging
    t.console = true
end
